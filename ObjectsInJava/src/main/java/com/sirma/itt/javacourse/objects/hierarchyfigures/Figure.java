/**
 * The abstract class Figure contains only one abstract method draw and stays on the top of the
 * hierarchy of figures.
 * 
 * @author Svetlosar Kovatchev
 * @version 1.0
 */
public abstract class Figure {
	/**
	 * Draws a figure.
	 */
	abstract void draw();

}
